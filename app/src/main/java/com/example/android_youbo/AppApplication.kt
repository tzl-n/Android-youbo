package com.example.android_youbo

import android.app.Application
import android.content.Context
import android.util.Log
import com.example.android_youbo.utils.MMKVUtils
import com.tencent.bugly.crashreport.CrashReport

class AppApplication : Application() {
    companion object {
        private var instance: AppApplication? = null
        
        fun getInstance(): AppApplication {
            return instance ?: throw IllegalStateException("AppApplication not initialized")
        }
        
        fun getApplicationContext(): Context {
            return instance?.applicationContext ?: throw IllegalStateException("AppApplication not initialized")
        }
    }

    override fun onCreate() {
        super.onCreate()
        instance = this
        Log.d("AppApplication", "Application created")

        // 可以在这里初始化全局配置、第三方SDK等
        initializeGlobalConfig()


    }

    private fun initializeGlobalConfig() {
        // 初始化全局配置
        Log.d("AppApplication", "Initializing global configuration...")
        
        // 初始化 MMKV
        MMKVUtils.init(this)
        Log.d("AppApplication", "MMKV initialized")
        
        // 初始化 Bugly
        CrashReport.initCrashReport(getApplicationContext(), "b7ca37f2c4", false)
        Log.d("AppApplication", "Bugly initialized")
    }





















































































































































































































































































































































































































































































































}